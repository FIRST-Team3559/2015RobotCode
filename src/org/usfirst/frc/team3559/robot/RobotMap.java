package org.usfirst.frc.team3559.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
 
	// Set values for PWM ports
	public static int LEFT_MOTOR = 8;
	public static int RIGHT_MOTOR = 9;
    public static int WINCH_MOTOR = 0;
    
    // Set values for DIO ports
    public static int TEST_LIMIT = 0;
    
    // Set values for Analog ports
    public static int GYRO = 0;
 

    }
